task scheduler { //Schedules the startup of the bot and user-created tasks
	bLCDBacklight = true;
	startTask(autoled);
	startTask(lcd); //Starts with allowing us to select the autonomous with LCD
	waitUntil(autoselect==1||autostart==1);
	stopTask(lcd);
	stopTask(autoled);
	startTask(batteryled);
}
